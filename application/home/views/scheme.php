				<p>If you avail housing loan for purchase/construction of new house or for extension of an existing house, government pays part of the interest on loan. This contribution of Government is called Credit Linked Subsidy. The scheme outlining how and when and to whom the subsidy is to be given is called credit linked subsidy scheme. The contribution of Government depends upon certain conditions.
